package com.sohoki.backoffice.sym.avaya.vo;

public class AvayaMessageInfo {

	private String requestid = "";
	private String reqMessage = "";
	private String reqMessageTxt = "";
	
	private String resMessage = "";
	private String resMessageTxt = "";
	
	private String reqRegdate = "";
	
	
	
	

	public String getRequestid() {
		return requestid;
	}

	public void setRequestid(String requestid) {
		this.requestid = requestid;
	}

	public String getReqMessage() {
		return reqMessage;
	}

	public void setReqMessage(String reqMessage) {
		this.reqMessage = reqMessage;
	}

	public String getReqMessageTxt() {
		return reqMessageTxt;
	}

	public void setReqMessageTxt(String reqMessageTxt) {
		this.reqMessageTxt = reqMessageTxt;
	}

	public String getResMessage() {
		return resMessage;
	}

	public void setResMessage(String resMessage) {
		this.resMessage = resMessage;
	}

	public String getResMessageTxt() {
		return resMessageTxt;
	}

	public void setResMessageTxt(String resMessageTxt) {
		this.resMessageTxt = resMessageTxt;
	}

	public String getReqRegdate() {
		return reqRegdate;
	}

	public void setReqRegdate(String reqRegdate) {
		this.reqRegdate = reqRegdate;
	}
	
	
	
	
}
